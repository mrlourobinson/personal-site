<div class="bio">
    <h1>About Lou Robinson</h1>

    <p>I'm Lou Robinson, a designer and visual journalist on the <a href="https://edition.cnn.com/profiles/lou-robinson" target="_blank" rel="noopener noreferrer">CNN Data and Graphics team</a>. I work on stories ranging from climate change impacts to breaking news, creating maps, charts, and interactive graphics that help people understand complex issues.</p>

    <p>My work has been recognized with a <a href="https://winners.lovieawards.com/categories/details/2025/websites-mobile-sites/websites-mobile-sites-general/news-politics/" target="_blank" rel="noopener noreferrer">Lovie Award</a> for Best News & Politics Website.</p>

    <p>After studying Graphic Design at Falmouth University, I worked in various roles, ranging from fashion startups, financial reporting, and the UK government. I spent nearly a decade combining design, data and cartography in "rooms filled with news", before finally making my way into journalism and a real newsroom. I love to combine design, data and mapping with storytelling on topics that really matter.</p>

    <p>I'm originally from Teesside in North East England, and currently based in London.</p>
